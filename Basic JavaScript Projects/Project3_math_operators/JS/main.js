// دالة لعملية الضرب
function multiplication_Function() {
    var result = 8 * 6; // ضرب رقمين معاً
    document.getElementById("Math").innerHTML = "8 × 6 = " + result;
}

// دالة لعملية القسمة
function division_Function() {
    var result = 48 / 6; // قسمة رقمين معاً
    document.getElementById("Math").innerHTML = "48 ÷ 6 = " + result;
}
